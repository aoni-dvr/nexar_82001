/*-----------------------------------------------------------------------------------------------*\
 *  @RoutineName:: AmbaB8_CodecGetVariableBitRate
 *
 *  @Description:: Calculate variable bit rate for CFA Codec
 *
 *  @Input      ::
 *     PictureWidth:        Picture width of one exposure picture
 *     NumFarSensors:       Number of sensors on a B8F
 *     SerdesDataRate:      Serdes data rate (in unit of bps)
 *     RowTime:             row time of pictures from a sensor (assum that HDR lines are interleaved)
 *
 *  @Output     :: none
 *
 *  @Return     ::
 *     UINT16 : Target bitrate (in unit of 1/1024 bits/pixel), max value is 12*1024=12288
\*-----------------------------------------------------------------------------------------------*/
UINT16 AmbaB8_CodecGetVariableBitRate(UINT16 PictureWidth, UINT8 NumFarSensors, UINT32 SerdesDataRate, FLOAT RowTime)
{
    DOUBLE TmpTargetBitRate;
    DOUBLE VariableBitRateDb;
    UINT16 VariableBitRate;
    UINT32 ECC_t = 1U;             /* Only 0, 1, 4, 8 are valid. (0 means no ECC).*/
    UINT32 SegLengthMinus1InByte ; /* payload size of a segment in unit of byte (each unit is 24-bit (i.e. 3-byte)) */
    UINT32 PacketSizeInByte;

    SegLengthMinus1InByte = (B8_CodecDefaultParams[8] & 0x1FFU) * 3U;

    TmpTargetBitRate = (((DOUBLE)SerdesDataRate * 1000000.0 * 0.8 * 0.93 * RowTime) / (DOUBLE)PictureWidth) / (DOUBLE)NumFarSensors;
    PacketSizeInByte = (12U + 7U + (2U * ECC_t) + (SegLengthMinus1InByte + 3U));
    (void) AmbaB8_Wrap_floor((DOUBLE)TmpTargetBitRate * (((DOUBLE)SegLengthMinus1InByte / (DOUBLE)PacketSizeInByte) * (DOUBLE)1024.0), &VariableBitRateDb);
    VariableBitRate = (UINT16)VariableBitRateDb;

    return (VariableBitRate > 12288U) ? 12288U : VariableBitRate;
}

/*-----------------------------------------------------------------------------------------------*\
 *  @RoutineName:: AmbaB8_CodecConfig
 *
 *  @Description:: Configure B8 CFA Codec
 *
 *  @Input      ::
 *     ChipID:        B8 chip id
 *     pCodecConfig:  Pointer to CFA Codec configuration
 *
 *  @Output     :: none
 *
 *  @Return     ::
 *      UINT32 : OK(0)/NG(-1)
\*-----------------------------------------------------------------------------------------------*/
UINT32 AmbaB8_CodecConfig(UINT32 ChipID, UINT8 CodecMode, const B8_CODEC_CONFIG_s *pCodecConfig)
{
    UINT32 i, Quotient;
    UINT32 Addr = B8_AHB_CFG_CODEC_BASE_ADDR;
    UINT32 DataBuf32[B8_CODEC_WR_CHUNK_SIZE];
    B8_CODEC_CTRL_REG_s Ctrl = {0};
    B8_CODEC_SENSOR_CTRL_REG_s SensorCtrl = {0};
    B8_CODEC_PIC_SIZE_REG_s PictureSize = {0};

    Quotient = B8_CODEC_NUM_DEFAULT_REG / B8_CODEC_WR_CHUNK_SIZE;

    for (i = 0U; i < Quotient; i++) {
        (void) AmbaB8_Wrap_memcpy(DataBuf32, &B8_CodecDefaultParams[i * B8_CODEC_WR_CHUNK_SIZE], sizeof(DataBuf32));
        (void) AmbaB8_RegWriteU32(ChipID, Addr, 1, B8_DATA_WIDTH_32BIT, (INT32)B8_CODEC_WR_CHUNK_SIZE, DataBuf32);
        Addr += ((UINT32)B8_CODEC_WR_CHUNK_SIZE << 2U);
    }

    if (CodecMode == B8_CODEC_ENCODER) {
        /* (scratchpad) */
        DataBuf32[0] = 0xc0030000U;
        (void) AmbaB8_RegWrite(ChipID, & pAmbaB8_ScratchpadReg->HighSpeedBypass, 0, B8_DATA_WIDTH_32BIT, 1, &DataBuf32[0]);

        /* Codec Ctrl */
        Ctrl.Stream23EnDec = 0U;
        Ctrl.Stream01EnDec = 0U;
        Ctrl.ClkDisDec = 1U;
        Ctrl.ClkDisEnc = 0U;
        Ctrl.LegacyDecmpProtocol = 1U;
        Ctrl.SwReset = 0U;
        Ctrl.LegacyCmp = 0U;
        Ctrl.EncodeMode = 1U;
        Ctrl.Enable = 0U;

        /* Codec Sensor Ctrl */
        SensorCtrl.RightPicture = 0U;
        SensorCtrl.NumFarSensorMinus1 = 0U;
        SensorCtrl.InputBitMinus8 = pCodecConfig->NumInputBits - 8U;
        SensorCtrl.SplitHorizontal = pCodecConfig->SplitHorizontal;
        SensorCtrl.NumExpMinus1 = (UINT8)pCodecConfig->NumPicturesPerFrame - 1U;
        SensorCtrl.NumSensorMinus1 = pCodecConfig->NumFarSensors - 1U;
        SensorCtrl.Bayer = pCodecConfig->CfaType;
    } else {
        /* (scratchpad) */
        DataBuf32[0] = 0xc3030000U;
        (void) AmbaB8_RegWrite((ChipID & B8_MAIN_CHIP_ID_B8NF_MASK), & pAmbaB8_ScratchpadReg->HighSpeedBypass, 0, B8_DATA_WIDTH_32BIT, 1, &DataBuf32[0]);

        /* Codec Ctrl */
        Ctrl.Stream23EnDec = (pCodecConfig->SplitHorizontal == 0U) ? 0U : 1U;
        Ctrl.Stream01EnDec = 1U;
        Ctrl.ClkDisDec = 0U;
        Ctrl.ClkDisEnc = 1U;
        Ctrl.LegacyDecmpProtocol = 1U;
        Ctrl.SwReset = 0U;
        Ctrl.LegacyCmp = 0U;
        Ctrl.EncodeMode = 0U;
        Ctrl.Enable = 0U;

        /* Codec Sensor Ctrl */
        SensorCtrl.RightPicture = 0U;
        SensorCtrl.NumFarSensorMinus1 = pCodecConfig->NumFarSensors - 1U;
        SensorCtrl.InputBitMinus8 = pCodecConfig->NumInputBits - 8U;
        SensorCtrl.SplitHorizontal = pCodecConfig->SplitHorizontal;
        SensorCtrl.NumExpMinus1 = pCodecConfig->NumPicturesPerFrame - 1U;
        SensorCtrl.NumSensorMinus1 = 0U;
        SensorCtrl.Bayer = pCodecConfig->CfaType;
    }

    if ((pCodecConfig->NumFarSensors == 1U) && (pCodecConfig->SplitHorizontal == 1U)) {
        SensorCtrl.RightPicture = 0x2U;
    }

    PictureSize.WidthDiv4Minus1 = (pCodecConfig->PictureWidth / 4U) - 1U;
    PictureSize.HeightDiv2Minus1 = (pCodecConfig->PictureHeight / 2U) - 1U;

    (void) AmbaB8_Wrap_memcpy(&DataBuf32[0], &SensorCtrl, sizeof(B8_CODEC_SENSOR_CTRL_REG_s));
    (void) AmbaB8_Wrap_memcpy(&DataBuf32[1], &PictureSize, sizeof(B8_CODEC_PIC_SIZE_REG_s));
    (void) AmbaB8_RegWrite(ChipID, &pAmbaB8_CodecReg->SensorCtrl, 1, B8_DATA_WIDTH_32BIT, 2, DataBuf32);

    DataBuf32[0] = ((UINT32)pCodecConfig->TargetBitRate << 16U) | (UINT32)pCodecConfig->TargetBitRate;
    DataBuf32[1] = DataBuf32[0];
    DataBuf32[2] = DataBuf32[0];
    (void) AmbaB8_RegWrite(ChipID, &pAmbaB8_CodecReg->Set0VbRate0, 1, B8_DATA_WIDTH_32BIT, 3, DataBuf32);

    (void) AmbaB8_Wrap_memcpy(&DataBuf32[0], &Ctrl, sizeof(B8_CODEC_CTRL_REG_s));
    Ctrl.Enable = 1U;
    (void) AmbaB8_Wrap_memcpy(&DataBuf32[1], &Ctrl, sizeof(B8_CODEC_CTRL_REG_s));
    (void) AmbaB8_RegWrite(ChipID, &pAmbaB8_CodecReg->Ctrl, 0, B8_DATA_WIDTH_32BIT, 2, DataBuf32);

    return B8_ERR_NONE;
}
